            ':salt' => $salt, //store this number for later use//
            //It will take the registration details and add them to the database (all four, post data already taken)//
            ':email' => $_POST['email']
        );
        try {
            $stmt = $db->prepare($query);
            $result = $stmt->execute($query_params);
        }
